function openNav() {
    document.getElementById("Nav-left").style.width = "240px";
    document.getElementById("main").style.marginLeft = "240px";
  }
  
  function closeNav() {
    document.getElementById("Nav-left").style.width = "0";
    document.getElementById("main").style.marginLeft= "0";
  }

